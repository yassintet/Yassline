export function translateCircuit(circuit: any, language: 'es' | 'en' | 'fr') {
  // Verificar que el circuito tenga un nombre válido
  if (!circuit || !circuit.name) {
    console.warn('⚠️ Circuit missing name:', circuit);
    return circuit;
  }

  const translation = circuitTranslations[circuit.name];
  if (!translation) {
    // Si no hay traducción, devolver datos originales
    console.warn(`⚠️ No translation found for circuit: ${circuit.name}`);
    return circuit;
  }

  const translated = {
    ...circuit,
    name: translation[language].name,
    title: translation[language].title,
    description: translation[language].description,
    duration: translation[language].duration
  };

  return translated;
}
